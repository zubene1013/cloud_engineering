package exam04_연산자;

public class Operator03_비교연산자 {

	public static void main(String[] args) {

		int n = 10;
		int n2 = 3;
	
		//실행결과를 변수에 저장 가능
		boolean result = n == n2;
		System.out.println(result);
		
		System.out.println(n == n2); // n과 n2값이 같냐?
		System.out.println(n != n2); 
		System.out.println(n > n2); 
		System.out.println(n >= n2); 
		System.out.println(n < n2); 
		System.out.println(n <= n2); 
		
		
		
	}

}
