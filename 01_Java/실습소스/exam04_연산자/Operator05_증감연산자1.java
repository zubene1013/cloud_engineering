package exam04_연산자;

public class Operator05_증감연산자1 {

	public static void main(String[] args) {

		// 전치 연산자
		int n = 10;
		++n; // n=n+1 동일,  n+=1 동일
		System.out.println(n);
		
		--n; // n=n-1 동일,  n-=1 동일
		System.out.println(n);
		
		
		//후치 연산자
		int n2 = 10;
		n2++; // n=n+1 동일,  n+=1 동일
		System.out.println(n2);
		
		n2--; // n=n-1 동일,  n-=1 동일
		System.out.println(n2);
		
		
		
		
	}

}
