package exam01_기본형;

public class PDT02_float {

	public static void main(String[] args) {
		
		/*
		    실수:
		    float (4byte)
		      3.13F, 3.14f 
		      
		    double (8byte)
		 
		 */
		//float
		System.out.println(3.14F);
		System.out.println(3.14f);
		
		System.out.println("=====================================");
		//double
		System.out.println(3.14D);
		System.out.println(3.14d);
		System.out.println(3.14);
		
	}
}
